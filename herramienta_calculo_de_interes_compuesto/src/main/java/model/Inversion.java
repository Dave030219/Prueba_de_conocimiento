package model;

import java.util.List;

public class Inversion {
	private float gi;
	private float mf;
	private List<Salida> salida;
	
	public float getGi() {
		return gi;
	}
	public void setGi(float gi) {
		this.gi = gi;
	}
	public float getMf() {
		return mf;
	}
	public void setMf(float mf) {
		this.mf = mf;
	}
	public List<Salida> getSalida() {
		return salida;
	}
	public void setSalida(List<Salida> salida) {
		this.salida = salida;
	}
}
