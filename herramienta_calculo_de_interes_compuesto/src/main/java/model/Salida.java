package model;

public class Salida {
	private float anio;
	private float saldoI;
	private float aportacion;
	private float rendimiento;
	private float saldoF;
	
	public float getAnio() {
		return anio;
	}
	public void setAnio(float anio) {
		this.anio = anio;
	}
	public float getSaldoI() {
		return saldoI;
	}
	public void setSaldoI(float saldoI) {
		this.saldoI = saldoI;
	}
	public float getAportacion() {
		return aportacion;
	}
	public void setAportacion(float aportacion) {
		this.aportacion = aportacion;
	}
	public float getRendimiento() {
		return rendimiento;
	}
	public void setRendimiento(float rendimiento) {
		this.rendimiento = rendimiento;
	}
	public float getSaldoF() {
		return saldoF;
	}
	public void setSaldoF(float saldoF) {
		this.saldoF = saldoF;
	}
}
